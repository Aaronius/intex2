package edu.byu.isys413.group1a.intex2.BOs;

/**
 * Allows a membership to be one of four different types
 *
 * @author Group 1A, isys@aaronhardy.com
 */
public class MembershipType extends BusinessObject {
  private String description = null;
  private float price = 0;
  private int numAllowedOut = 0;
  
  /** Creates a new instance of MembershipType */
  public MembershipType() {
  }
  /** Gets description of membership */
  public String getDescription() {
    return description;
  }
  /** Sets description of membership */
  public void setDescription(String description) {
    this.description = description;
  }
  /** Gets price of membership */
  public float getPrice() {
    return price;
  }
  /** Sets price of membership */
  public void setPrice(float price) {
    this.price = price;
  }
  /** Gets number of included rentals for membership */
  public int getNumAllowedOut() {
    return numAllowedOut;
  }
  /** Sets number of included rentals for membership */
  public void setNumAllowedOut(int numAllowedOut) {
    this.numAllowedOut = numAllowedOut;
  }
 
}//class
