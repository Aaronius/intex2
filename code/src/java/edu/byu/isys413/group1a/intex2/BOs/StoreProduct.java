package edu.byu.isys413.group1a.intex2.BOs;

/**
 * A Store Product
 *
 * @author Group 1A, isys@aaronhardy.com
 */
public class StoreProduct extends BusinessObject {
  private Store store = null;
  private Product prod = null;
  private int qtyOnHand = 0;
  private int reorderPoint = 0;
  private int reorderQTY = 0;
  
  /** Creates a new instance of StoreProduct */
  public StoreProduct() {
  }
  /** Gets store */
  public Store getStore() {
    return store;
  }
  /** Sets store */
  public void setStore(Store store) {
    this.store = store;
  }
  /** Gets product */
  public Product getProd() {
    return prod;
  }
  /** Sets product */
  public void setProd(Product prod) {
    this.prod = prod;
  }
  /** Gets quantity on hand */
  public int getQtyOnHand() {
    return qtyOnHand;
  }
  /** Sets quantity on hand */
  public void setQtyOnHand(int qtyOnHand) {
    this.qtyOnHand = qtyOnHand;
  }
  /** Gets reorder point */
  public int getReorderPoint() {
    return reorderPoint;
  }
  /** Sets reorder point */
  public void setReorderPoint(int reorderPoint) {
    this.reorderPoint = reorderPoint;
  }
  /** Gets reorder point */
  public int getReorderQTY() {
    return reorderQTY;
  }
  /** Sets reorder point */
  public void setReorderQTY(int reorderQTY) {
    this.reorderQTY = reorderQTY;
  }
  
}//class
