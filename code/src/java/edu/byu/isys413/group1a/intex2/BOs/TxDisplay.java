package edu.byu.isys413.group1a.intex2.BOs;

/**
 * Transaction Display used to carry information from business objects
 * to the GUI Display
 *
 * @author Group 1A, isys@aaronhardy.com
 */
public class TxDisplay {
  
  private double subTotal = 0;
  private double tax = 0;
  private double total = 0;
  private double balance = 0;
  private double minAmtDue = 0;
  private double payment = 0;
  private double balancePayment = 0;
  private double change = 0;
  
  /** Creates a new instance of TxDisplay */
  public TxDisplay() {
  }
  
  //** Gets subTotal */
  public double getSubTotal() {
    return subTotal;
  }
  
  //** Sets subTotal */
  public void setSubTotal(double subTotal) {
    this.subTotal = subTotal;
  }
  
  //** Gets tax */
  public double getTax() {
    return tax;
  }
  
  //** Sets tax */
  public void setTax(double tax) {
    this.tax = tax;
  }
  
  //** Gets total */
  public double getTotal() {
    return total;
  }
  
  //** Sets total */
  public void setTotal(double total) {
    this.total = total;
  }
  
  //** Gets total */
  public double getBalance() {
    return balance;
  }
  
  //** Sets total */
  public void setBalance(double balance) {
    this.balance = balance;
  }
  
  //** Gets minAmtDue */
  public double getMinAmtDue() {
    return minAmtDue;
  }
  
  //** Sets minAmtDue */
  public void setMinAmtDue(double minAmtDue) {
    this.minAmtDue = minAmtDue;
  }
  
  /** Gets payment **/
  public double getPayment() {
    return payment;
  }
  
  /** Sets payment **/
  public void setPayment(double payment) {
    this.payment = payment;
  }
  
  /** Gets balancePayment **/
  public double getBalancePayment() {
    return balancePayment;
  }
  
  /** Sets payment **/
  public void setBalancePayment(double balancePayment) {
    this.balancePayment = balancePayment;
  }
  
  //** Gets change */
  public double getChange() {
    return change;
  }
  
  //** Sets change */
  public void setChange(double change) {
    this.change = change;
  }
  
  public void print(){
    System.out.println("total" + total);
  }
  
}//class
