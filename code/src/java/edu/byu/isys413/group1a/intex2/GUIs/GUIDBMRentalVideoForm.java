  private void addModifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addModifyButtonActionPerformed
    
    // Validate serial number
    if (!serialNumberDigitCheck()) return;
    if (!serialNumberUniqueCheck()) return;
    
    if (rentalVideo == null){ // This is a new rental video
      
      // Save the new rental video
      try{
        controller.saveRentalVideo(
                serialNumberField.getText(),
                statusBox.getSelectedItem(),
                conceptualVideoBox.getSelectedItem(),
                releaseTypeBox.getSelectedItem(),
                videoCategoryBox.getSelectedItem(),
                storeBox.getSelectedItem());
      } catch(Exception e){
        JOptionPane.showMessageDialog(this, "An error occured while saving rental video information.", "Error Saving Rental Video", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
      }
      
    } else { // This is an existing rental video
      
      // Save the modified rental video
      try{
        controller.saveRentalVideo(
                rentalVideo,
                serialNumberField.getText(),
                statusBox.getSelectedItem(),
                conceptualVideoBox.getSelectedItem(),
                releaseTypeBox.getSelectedItem(),
                videoCategoryBox.getSelectedItem(),
                storeBox.getSelectedItem());
      } catch(Exception e){
        JOptionPane.showMessageDialog(this, "An error occured while saving rental video information.", "Error Saving Rental Video", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
      }
      
    }
    
    // Save successful.  Clean up.
    JOptionPane.showMessageDialog(this, "The rental video has been saved." , "Rental Video Saved Successfully", JOptionPane.PLAIN_MESSAGE);
    this.dispose(); // dispose of the current window and all its resources
  }//GEN-LAST:event_addModifyButtonActionPerformed
  
